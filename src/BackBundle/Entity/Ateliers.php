<?php

namespace BackBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Ateliers
 *
 * @ORM\Table(name="ateliers")
 * @ORM\Entity(repositoryClass="BackBundle\Repository\AteliersRepository")
 */
class Ateliers
{
